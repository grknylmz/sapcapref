package com.sap.exciseduty.extpts.slgd;

public class SpecialPartnerType {

    String id;
    String taxationRelevantIndicator;
    String description;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTaxationRelevantIndicator() {
        return taxationRelevantIndicator;
    }

    public void setTaxationRelevantIndicator(String taxationRelevantIndicator) {
        this.taxationRelevantIndicator = taxationRelevantIndicator;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

}
